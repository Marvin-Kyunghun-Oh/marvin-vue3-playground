<template>
  <div>
    <h1>watchEffectлҘј нҶөн•ҙ кёҖмқ„ м ҖмһҘн–Ҳмқ„ л•Ң мһҗлҸҷмңјлЎң м ҖмһҘн•ҳлҸ„лЎқ н•ңлӢӨ.</h1>
    <form action="" @submit.prevent="save(title, contents)">
      <input type="text" v-model.lazy="title" placeholder="мһ…л Ҙн•ҳм„ёмҡ”."/><br>
      <textarea v-model.lazy="contents" placeholder="мһ…л Ҙн•ҳм„ёмҡ”..."></textarea>
      <hr />
      <button>м ҖмһҘ</button>
    </form>
  </div>
</template>

<script>
import { ref, watchEffect } from 'vue';

export default {
  setup () {
    
    const title = ref('');
    const contents = ref('');

    const save = (title, contents) => {
      console.log(`мһҗлҸҷм ҖмһҘ. title: ${title}, contents: ${contents}`);
    };

    watchEffect(() => {
      save(title.value, contents.value);
    });

    return {
      title,
      contents,
      save,
    }
  }
}
</script>

